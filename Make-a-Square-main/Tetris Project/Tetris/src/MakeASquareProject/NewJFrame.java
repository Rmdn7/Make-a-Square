    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Grid g = new Grid();
        Methods method = new Methods();
        g.gridClear();
        wantedL = Integer.parseInt(jTextField1.getText());
        wantedZ = Integer.parseInt(jTextField2.getText());
        wantedS = Integer.parseInt(jTextField3.getText());
        wantedT = Integer.parseInt(jTextField4.getText());
        wantedI = Integer.parseInt(jTextField5.getText());
        wantedO = Integer.parseInt(jTextField6.getText());
        
        // check validity
        boolean validityFlag = method.checkValidity(wantedL, wantedT, wantedZ, wantedS, wantedI, wantedO);
        boolean areaFlag = method.checkArea(wantedL, wantedT, wantedZ, wantedS, wantedI, wantedO);
        Grid.validQuery = validityFlag && (!areaFlag);
        
        //
        method.setValues(wantedL, wantedZ, wantedS, wantedT, wantedI, wantedO);
        while(Grid.validQuery){
            method.runProgram();
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(!isValidGrid(Grid.finalGrid)){
                color();
                JOptionPane.showMessageDialog(this, "No solution is found :(");
                break;
            }
            else{
                g.printFinalGrid();
                color();
                try {
                Thread.sleep(500);
                } catch (InterruptedException ex) {
                    Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
            }
        }
        if(!Grid.validQuery){
            color();
            JOptionPane.showMessageDialog(this, "No solution is found :(");
        }
        reset();
    }//GEN-LAST:event_jButton1ActionPerformed

