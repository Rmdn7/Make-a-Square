    List <Color> genColors(){
        int count = 1000;

        List<Color> colors = new ArrayList<>();
        for (int i = 0; i < count; i++) {
            float hue = (float) Math.random() + 3;
            float saturation = (float) Math.random() + 5;
            float brightness = (float) Math.random() + 7;
            colors.add(Color.getHSBColor(hue, saturation, brightness));
        }

        Collections.shuffle(colors);

        return colors;
    }
    public boolean color(){

        boolean notValid = false;
        for(int i = 0; i < 4; i++){
            for(int j = 0; j < 4; j++){
                if(Grid.finalGrid[i][j] == 0)
                    notValid = true;
            }
        }
        boolean ret = true;
        if(notValid){
            colorUnvalid();
            ret = false;
        }
        else{
            colorValid();
        }
        return ret;
    }
    public boolean isValidGrid(int[][] grid){
        for(int i = 0; i < 4; i++){
            for(int j = 0; j < 4; j++)
            {
                if(grid[i][j] == 0)
                    return false;
            }
        }
        return true;
    }
